    public void readJTableForDescProd(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) tablePesquisaProduto.getModel();
        modelo.setNumRows(0);
        ProdutosDAO pdao = new ProdutosDAO();

        for (Produtos p : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                p.getCodigoProduto(),
                p.getDescricao(),
                p.getPreco(),
                p.getUnidade()
            });

        }
    }
    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) tablePesquisaProduto.getModel();
        modelo.setNumRows(0);
        ProdutosDAO pdao = new ProdutosDAO();

        for (Produtos p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getCodigoProduto(),
                p.getDescricao(),
                p.getPreco(),
                p.getUnidade()
            });

        }

    }

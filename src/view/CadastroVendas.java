    private void buttonAddProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddProdutoActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tableProdutosVendas.getModel();
        ProdutosDAO pdao = new ProdutosDAO();

        for (Produtos p : pdao.readForDesc((String) tablePesquisaProduto.getValueAt(tablePesquisaProduto.getSelectedRow(), 1))) {

            modelo.addRow(new Object[]{
                p.getCodigoProduto(),
                p.getDescricao(),
                spinnerAddProduto.getValue(),
                p.getPreco()
                
            });
        }
        int soma=0;
        int contador = tableProdutosVendas.getRowCount();
        for(int i=0; i<contador; i++){
             soma =  soma + (Integer.valueOf(tableProdutosVendas.getValueAt(i,3).toString())*Integer.valueOf(tableProdutosVendas.getValueAt(i,2).toString()));
       }
       
        txtValorTotal.setText(String.valueOf(soma));
        int Pagar = soma - Integer.valueOf(txtDesconto.getText());
        txtAPagar.setText(String.valueOf(Pagar));
        
        
        
   
        
        
    }//GEN-LAST:event_buttonAddProdutoActionPerformed


